"""
LangChain dialogue with history for FastAPI /chat.
"""
from __future__ import annotations

import os
import threading
from typing import List

from langchain_core.messages import AIMessage, BaseMessage, HumanMessage
from langchain_core.output_parsers import StrOutputParser
from langchain_core.prompts import ChatPromptTemplate, MessagesPlaceholder
from langchain_openai import ChatOpenAI

_api_key = os.getenv("DEEPSEEK_API_KEY", "").strip()
if not _api_key:
    raise RuntimeError(
        "Missing DEEPSEEK_API_KEY. Example (PowerShell): $env:DEEPSEEK_API_KEY='sk-...'"
    )

llm = ChatOpenAI(
    model=os.getenv("DEEPSEEK_MODEL", "deepseek-chat"),
    openai_api_key=_api_key,
    openai_api_base=os.getenv("DEEPSEEK_API_BASE", "https://api.deepseek.com"),
    temperature=float(os.getenv("DEEPSEEK_TEMPERATURE", "0.7")),
)

_sessions: dict[str, List[BaseMessage]] = {}
_session_locks: dict[str, threading.Lock] = {}
_registry_lock = threading.Lock()

MAX_CHAT_ROUNDS = max(1, int(os.getenv("CHAT_MAX_ROUNDS", "12")))

SYSTEM_PROMPT = """????????????????????? {language} ???

?????????????????
- ????????????????????????????????????URL ?? /detect-task?
- ????????????????????????????/history?
- ???????????????????????/disaster-feed?
- ?????????????/profile?

????????????????????????????
{record_context}

???????????????????????????????????????????????

??????
1. ?????????????????????? I?IV????????????????????
2. ????????????????????????????????????????????????
3. ?????????????????"""

prompt_template = ChatPromptTemplate.from_messages(
    [
        ("system", SYSTEM_PROMPT),
        MessagesPlaceholder(variable_name="history"),
    ]
)

parser = StrOutputParser()


def _session_key(session_id: str | None) -> str:
    key = (session_id or "").strip()
    return key if key else "default"


def _lock_for(key: str) -> threading.Lock:
    with _registry_lock:
        if key not in _session_locks:
            _session_locks[key] = threading.Lock()
        return _session_locks[key]


def _trim_chat_history(hist: List[BaseMessage]) -> None:
    max_messages = MAX_CHAT_ROUNDS * 2
    if len(hist) <= max_messages:
        return
    hist[:] = hist[-max_messages:]
    while hist and isinstance(hist[0], AIMessage):
        hist.pop(0)


def record_and_invoke(
    user_input: str,
    language: str = "??",
    session_id: str | None = None,
    record_context: str | None = None,
) -> str:
    text = (user_input or "").strip()
    if not text:
        return ""

    ctx = (record_context or "").strip()
    if not ctx:
        ctx = "????????????????????????"

    key = _session_key(session_id)
    lock = _lock_for(key)

    with lock:
        hist = _sessions.setdefault(key, [])
        messages_for_turn = list(hist) + [HumanMessage(content=text)]

        prompt_data = {
            "language": language,
            "record_context": ctx,
            "history": messages_for_turn,
        }

        chain = prompt_template | llm | parser
        response = chain.invoke(prompt_data)
        reply = (response or "").strip()

        hist.append(HumanMessage(content=text))
        hist.append(AIMessage(content=reply))
        _trim_chat_history(hist)
        return reply


def reset_history(session_id: str | None = None) -> None:
    key = _session_key(session_id)
    lock = _lock_for(key)
    with lock:
        _sessions[key] = []
